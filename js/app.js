// Рендер каталога
function renderCatalog() {
  const catalog = document.getElementById('catalog');
  catalog.innerHTML = assistants.map(a => `
    <div class="card" onclick="openModal(${a.id})">
      <div class="card-header">
        <span class="card-icon">${a.icon}</span>
        <span class="card-title">${a.title}</span>
      </div>
      <p class="card-desc">${a.desc}</p>
      <div class="card-footer">
        <span class="card-price">${a.price.toLocaleString('ru-RU')} ₽</span>
        <span class="card-tag">${a.tag}</span>
      </div>
    </div>
  `).join('');
}

// Открыть модалку
function openModal(id) {
  const a = assistants.find(x => x.id === id);
  if (!a) return;

  const body = document.getElementById('modal-body');
  body.innerHTML = `
    <div class="modal-icon">${a.icon}</div>
    <div class="modal-title">${a.title}</div>
    <div class="modal-tagline">${a.tagline}</div>
    <div class="modal-section">
      <h3>Что вы получите</h3>
      <ul class="modal-list">
        ${a.features.map(f => `<li>${f}</li>`).join('')}
      </ul>
    </div>
    <div class="modal-price">${a.price.toLocaleString('ru-RU')} ₽</div>
  `;

  const btn = document.getElementById('modal-btn');
  if (a.payUrl) {
    btn.href = a.payUrl;
    btn.classList.remove('disabled');
    btn.textContent = 'Купить';
  } else {
    btn.href = '#';
    btn.classList.add('disabled');
    btn.textContent = 'Скоро';
  }

  document.getElementById('modal').classList.remove('hidden');
}

// Закрыть модалку
function closeModal() {
  document.getElementById('modal').classList.add('hidden');
}

renderCatalog();
